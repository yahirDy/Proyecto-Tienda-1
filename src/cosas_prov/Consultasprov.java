    private void busActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_busActionPerformed
        // TODO add your handling code here:
        if(buscar2.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "No hay ningun id escrito");
        }
        else{
        int po = -1;
        ArrayList<Provedores> prov;
        DefaultTableModel m;
        prov = Archivo.leer("prov.dat");
        int id;
        id = Integer.parseInt(buscar2.getText());
        
        try {
            po = Busqueda.buscarIdprovArrayList(prov, id);
        } catch (IOException ex) {
            Logger.getLogger(Consultasprov.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(po != -1){
            JOptionPane.showMessageDialog(this,"El id consultado es: "+ id);
            prov.get(po);
            
            m = (DefaultTableModel)bu.getModel();
            Object[] rowData = new Object[6];

            rowData[0] = prov.get(po).getId();
            rowData[1] = prov.get(po).getNombre();
            rowData[2] = prov.get(po).getApellido();
            rowData[3] = prov.get(po).getMarca();
            rowData[4] = prov.get(po).getcT();
            rowData[5] = prov.get(po).gettot();
           m.addRow(rowData);
        }
        else{
            JOptionPane.showMessageDialog(this,"El id consultado: "+ id + "no existe");
        }
        
        }
        buscar2.setText(null);
    }//GEN-LAST:event_busActionPerformed

