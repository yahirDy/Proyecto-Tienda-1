    private void jButton_LOGINActionPerformed(java.awt.event.ActionEvent evt) {                                              
        PreparedStatement ps;
        ResultSet rs;
        String uname = jTextField1.getText();
        String pass = String.valueOf(jPasswordField1.getPassword());
        
        String query = "SELECT * FROM `usuarios` WHERE `usuario` =? AND `contrase√±a` =?";
        
        try {
            Conexionlogin con = new Conexionlogin();
            cnx = con.getConexionlogin();
            
            ps = cnx.prepareStatement(query);
            
            ps.setString(1, uname);
            ps.setString(2, pass);
            
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                    HOME_JFrame mf = new HOME_JFrame();
                    mf.setVisible(true);
                    mf.pack();
                    mf.jLabel2.setText("Bienvenid@ ");
                    mf.jLabel1.setText(uname);
                    
                    this.dispose();
            }
            else{
                    JOptionPane.showMessageDialog(null, "Usuario o Contrase√±a incorrecto", "No se pudo iniciar session ", 2);
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }                                             

