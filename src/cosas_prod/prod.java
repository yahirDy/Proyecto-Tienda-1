    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        if(id3.getText().length() == 0){
           JOptionPane.showMessageDialog(this, "Faltan campos a llenar");
        } 
        else if(nombrep.getText().length() == 0){
           JOptionPane.showMessageDialog(this, "Faltan campos a llenar");
        }
        else if(stock.getText().length() == 0){
           JOptionPane.showMessageDialog(this, "Faltan campos a llenar");
        }
        else if(preciop.getText().length() == 0){
           JOptionPane.showMessageDialog(this, "Faltan campos a llenar");
        }
        else{
        int po = -1;
        ArrayList<Producto> prodc;
        prodc = Archivo.leer("prodc.dat");
        int idces;
        idces = Integer.parseInt(id3.getText());
        
        try {
            po = Busqueda.buscarIdpArrayList(prodc, idces);
        } catch (IOException ex) {
            Logger.getLogger(prod.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(po != -1){
            JOptionPane.showMessageDialog(this,"El id: "+ idces + " ya existe por favor intoroduzca otro");
            id3.setText(null);
        }    
        else{  
        prodc = Archivo.leer("prodc.dat");
        int id3s, st; 
        float pre;
        String select = (String) cat.getSelectedItem();
        
        id3s = Integer.parseInt(id3.getText());
        st = Integer.parseInt(stock.getText());
        pre = Float.parseFloat(preciop.getText());

        prodc.add(new Producto(id3s, select, st, nombrep.getText(), pre));
        Archivo.guardar(prodc, "prodc.dat");
        
        JOptionPane.showMessageDialog(this, "Se ha dado de alta satisfacatoriamente");
        setVisible(false);
        }
       }
    }//GEN-LAST:event_guardarActionPerformed

