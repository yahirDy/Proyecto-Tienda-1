    private void ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarActionPerformed
        // TODO add your handling code here:
        if(idce.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "No hay ningun id escrito");
        }
        else{
        int po = 0;
        
        DefaultTableModel m;
        ArrayList<Empleado> emps;
        emps = Archivo.leer("emps.dat");
        int idces;
        idces = Integer.parseInt(idce.getText());
        try {
            po = Busqueda.buscarIdArrayList(emps, idces);
        } catch (IOException ex) {
            Logger.getLogger(Bajas.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(po != -1){
        emps.remove(po);
        JOptionPane.showMessageDialog(this,"El id: "+ idces + " a sido borrado exitosamente");
        
        }
        else{
        JOptionPane.showMessageDialog(this,"El id: "+ idces + " no existe");
        }
       
        Archivo.guardar(emps, "emps.dat");
        m = (DefaultTableModel)taco.getModel();
        Object[] rowData = new Object[7];
        
        for(int i = 0; i < taco.getRowCount(); i++){
            m.removeRow(i);
            i-=1;
        }
        
        if(m.getRowCount() == 0){
            for(Empleado e: emps){
                if(e instanceof Empleado){
                rowData[0] = ((Empleado)e).getId();
                rowData[1] = ((Empleado)e).getNom();
                rowData[2] = ((Empleado)e).getApe();
                rowData[3] = ((Empleado)e).getEdad();
                rowData[4] = ((Empleado)e).gethT();
                rowData[5] = ((Empleado)e).getpH();
                rowData[6] = ((Empleado)e).gethT() * ((Empleado)e).getpH();
                m.addRow(rowData);
                }    
            }
        }
        idce.setText(null);
    }
        
        
    }//GEN-LAST:event_ConsultarActionPerformed

