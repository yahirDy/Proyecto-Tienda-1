    private void busActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_busActionPerformed
        // TODO add your handling code here:
        if(buscar.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "No hay ningun id escrito");
        }
        else{
        int po = -1;
        DefaultTableModel m;
        ArrayList<Empleado> emps;
        emps = Archivo.leer("emps.dat");
        int id;
        id = Integer.parseInt(buscar.getText());
        try {
            po = Busqueda.buscarIdArrayList(emps, id);
        } catch (IOException ex) {
            Logger.getLogger(Consultas.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        if(po != -1){
                JOptionPane.showMessageDialog(this,"El id consultado es: "+ id);
                emps.get(po);
                
                m = (DefaultTableModel)bu.getModel();
                Object[] rowData = new Object[7];
                     
                rowData[0] = emps.get(po).getId();
                rowData[1] = emps.get(po).getNom();
                rowData[2] = emps.get(po).getApe();
                rowData[3] = emps.get(po).getEdad();
                rowData[4] = emps.get(po).gethT();
                rowData[5] = emps.get(po).getpH();
                rowData[6] = emps.get(po).gethT()* emps.get(po).getpH();
                m.addRow(rowData);    
            }
            else{
                JOptionPane.showMessageDialog(this,"El id consultado: "+ id + "no existe");    
            }
        }
        buscar.setText(null);
    }//GEN-LAST:event_busActionPerformed

