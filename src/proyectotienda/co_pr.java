    private void comp_proActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comp_proActionPerformed
        // TODO add your handling code here:
        if(id_emp.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Faltan campos a llenar");
        }
        else if(id_pro.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Faltan campos a llenar");
        }
        else if(ca_co_pro.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Faltan campos a llenar");
        }
        else{
        int po = -1;
        ArrayList<Empleado> emps;
        emps = Archivo.leer("emps.dat");
        int id, cop;
        id = Integer.parseInt(id_emp.getText());
        cop = Integer.parseInt(ca_co_pro.getText());
        
        
            try {
                po = Busqueda.buscarIdArrayList(emps, id);
            } catch (IOException ex) {
                Logger.getLogger(co_pr.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        
        if(po != -1){
            emps.get(po);
           JOptionPane.showMessageDialog(this,"El empleado que lo atiende es: "+ emps.get(po).getNom() + " " +emps.get(po).getApe());
            emps.get(po).getId();
            emps.get(po).getNom();
            emps.get(po).getApe();
            emps.get(po).getEdad();
                        
        }
        else{
            JOptionPane.showMessageDialog(this,"El id consultado: "+ id + "no existe");
            id_emp.setText(null);
        }
        
        int po1 = -1;
        ArrayList<Producto> prodc;
        DefaultTableModel m;
        prodc = Archivo.leer("prodc.dat");
        int idp;
        idp = Integer.parseInt(id_pro.getText());
        
        
            try {
                po1 = Busqueda.buscarIdpArrayList(prodc, idp);
            } catch (IOException ex) {
                Logger.getLogger(co_pr.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        
        if(po1 != -1){
            
            if(cop > prodc.get(po1).getStock()){
                JOptionPane.showMessageDialog(this, "No puede comprar mas de lo que hay en stock");
                ca_co_pro.setText(null);
            }
            else{
            prodc.get(po1);
            
            m = (DefaultTableModel)compras.getModel();
            Object[] rowData = new Object[7];

            prodc.get(po1).setStock(prodc.get(po1).getStock() - cop);
            
            rowData[0] = prodc.get(po1).getId();
            rowData[1] = prodc.get(po1).getCategoria();
            rowData[2] = prodc.get(po1).getStock();
            rowData[3] = prodc.get(po1).getNombre();
            rowData[4] = prodc.get(po1).getPrecio();
            rowData[5] = cop;
            rowData[6] = prodc.get(po1).getPrecio() * cop;
            m.addRow(rowData);
            
            Archivo.guardar(prodc, "prodc.dat");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"El id del producto: "+ id + "no existe");
            ca_co_pro.setText(null);
        }
        
        id_pro.setText(null);
        ca_co_pro.setText(null);
        }
    }//GEN-LAST:event_comp_proActionPerformed

